/*****************************************************************************
 枚举名    : WUEL2L3_CIPHER_ALGO_ENUM
 协议表格  : 10.3.3.4   Ciphering Algorithm
 ASN.1描述 : CipheringAlgorithm
 枚举说明  : 加密算法枚举定义
*****************************************************************************/
enum WUEL2L3_CIPHER_ALGO_ENUM
{
    WUEL2L3_CIPHER_ALGO_UEA0            = 0,
    WUEL2L3_CIPHER_ALGO_UEA1            = 1,
    WUEL2L3_CIPHER_ALGO_UEA2            = 2,

    WUEL2L3_CIPHER_ALGO_BUTT
};
typedef VOS_UINT8   WUEL2L3_CIPHER_ALGO_ENUM_UINT8;

/*****************************************************************************
   CCTRCH ID，MAC和WAS共同使用这个ID，
   WAS用来配置物理层，MAC用CCTRCH ID来匹配
   物理层上报的PHY_DATA_IND消息中的CCTRCHID，
   配置下发给物理层的PHY_DATA_REQ
*****************************************************************************/
enum WUEL2L3_CCTRCH_ENUM
{
    WUEL2L3_PCCPCH                      = 0,
    WUEL2L3_SCCPCH                      = 1,
    WUEL2L3_UL_DPCH                     = 2,
    WUEL2L3_PRACH                       = 3,
    WUEL2L3_DL_DPCH                     = 4,
    WUEL2L3_E_DCH                       = 5,
    WUEL2L3_HS_DSCH                     = 6,

    WUEL2L3_CCTRCH_BUTT
};
typedef VOS_UINT8   WUEL2L3_CCTRCH_ENUM_UINT8;

/*****************************************************************************
 结构名    : WUEL2L3_DIR_ENUM
 协议表格  :
 ASN.1描述 :
 结构说明  : 空口上下行方向
*****************************************************************************/
enum WUEL2L3_DIR_ENUM
{
    WUEL2L3_UL_DIR                       = 0,            /* uplink */
    WUEL2L3_DL_DIR                       = 1,            /* downlink */
    WUEL2L3_DIR_BUTT
};
typedef VOS_UINT8 WUEL2L3_DIR_ENUM_UINT8;

/*****************************************************************************
 结构名    : WUEL2L3_UL_MODE_ENUM
 协议表格  :
 ASN.1描述 :
 结构说明  : 上行数据传输模式
*****************************************************************************/
enum WUEL2L3_UL_MODE_ENUM
{
    WUEL2L3_UL_PCH_MODE                = 0,                /* CELL_PCH、URA_PCH */
    WUEL2L3_UL_FACH_RB0_MODE           = 1,                /* CELL_FACH用于CELL UPDATE、IDLE用于RRC CONNECTION REQUEST过程 */
    WUEL2L3_UL_NORMAL_MODE             = 2,                /* CELL_DCH、CELL_FACH非RB0 数传过程 */
    WUEL2L3_UL_MODE_BUTT
};
typedef VOS_UINT8 WUEL2L3_UL_MODE_ENUM_UINT8;

