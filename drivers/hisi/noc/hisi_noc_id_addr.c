/*
Function: noc_set_routeid_info
Description: set noc_routeid_info from platform_id
input: unsigned int platform_id -> platform_id input
output: none
return: 0 -> success
*/
int noc_set_routeid_info(unsigned int platform_id)
{
	switch (platform_id) {
	default:
	case HISI_NOC_HI3650:
		routid_ptr[0].ptr = hi3650_cfgsys_routeid_addr_tbl;
		routid_ptr[0].len = ARRAY_SIZE(hi3650_cfgsys_routeid_addr_tbl);
		routid_ptr[1].ptr = hi3650_vcodec_routeid_addr_tbl;
		routid_ptr[1].len = ARRAY_SIZE(hi3650_vcodec_routeid_addr_tbl);
		routid_ptr[2].ptr = hi3650_vivo_routeid_addr_tbl;
		routid_ptr[2].len = ARRAY_SIZE(hi3650_vivo_routeid_addr_tbl);
		break;
	case HISI_NOC_HI6250:
		routid_ptr[0].ptr = hi6250_cfgsys_routeid_addr_tbl;
		routid_ptr[0].len = ARRAY_SIZE(hi6250_cfgsys_routeid_addr_tbl);
		routid_ptr[1].ptr = hi6250_vcodec_routeid_addr_tbl;
		routid_ptr[1].len = ARRAY_SIZE(hi6250_vcodec_routeid_addr_tbl);
		routid_ptr[2].ptr = hi6250_vivo_routeid_addr_tbl;
		routid_ptr[2].len = ARRAY_SIZE(hi6250_vivo_routeid_addr_tbl);
		break;
	}
	return 0;
}

/* idx here should be the same as struct noc_bus_info noc_buses_info[MAX_NOC_BUSES_NR] in hisi_noc_bus.c */
unsigned int noc_find_addr_from_routeid(unsigned int idx, int initflow, int targetflow, int targetsubrange)
{
    unsigned int i;
    unsigned int count;
    const ROUTE_ID_ADDR_STRU* pastTbl;

	if (idx >= ARRAY_SIZE(routid_ptr))
		return 0;
	pastTbl = routid_ptr[idx].ptr;
	count = routid_ptr[idx].len;


    for(i = 0 ; i < count ; i++)
    {
        if ((pastTbl[i].init_flow == initflow) &&
        (pastTbl[i].targ_flow == targetflow) &&
        (pastTbl[i].targ_subrange == targetsubrange))
        return pastTbl[i].init_localaddr;
    }
    return 0;
}



